namespace Unchained.Ooxml.Drawing;

/// <summary>Specifies the shape used at the ends of a line (cap style).</summary>
public enum LineCapStyle
{
    /// <summary>The line ends exactly at the endpoint with a flat cap.</summary>
    Flat,
    /// <summary>The line extends beyond the endpoint with a square cap.</summary>
    Square,
    /// <summary>The line extends beyond the endpoint with a rounded cap.</summary>
    Round
}
