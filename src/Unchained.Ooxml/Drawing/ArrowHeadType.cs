namespace Unchained.Ooxml.Drawing;

/// <summary>
///     The shape of the arrowhead drawn at the end of a line.
/// </summary>
public enum ArrowHeadType
{
    /// <summary>No arrowhead.</summary>
    None,
    /// <summary>A standard filled triangle arrowhead.</summary>
    Triangle,
    /// <summary>A stealth (narrow, elongated) arrowhead.</summary>
    Stealth,
    /// <summary>A diamond-shaped arrowhead.</summary>
    Diamond,
    /// <summary>An oval-shaped arrowhead.</summary>
    Oval,
    /// <summary>An open (unfilled) arrowhead.</summary>
    Open
}
