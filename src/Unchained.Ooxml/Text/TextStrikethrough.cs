namespace Unchained.Ooxml.Text;

/// <summary>Strikethrough style applied to a text run.</summary>
public enum TextStrikethrough
{
    /// <summary>No strikethrough.</summary>
    None,
    /// <summary>A single horizontal line through the middle of the text.</summary>
    Single,
    /// <summary>Two parallel horizontal lines through the middle of the text.</summary>
    Double
}
