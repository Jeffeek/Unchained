namespace Unchained.Ooxml.Media;

/// <summary>MIME content types for embedded images.</summary>
internal static class MimeTypes
{
    /// <summary>PNG image MIME type.</summary>
    public const string Png = "image/png";

    /// <summary>JPEG image MIME type.</summary>
    public const string Jpeg = "image/jpeg";

    /// <summary>JPEG image MIME type (alias).</summary>
    public const string Jpg = "image/jpg";

    /// <summary>GIF image MIME type.</summary>
    public const string Gif = "image/gif";

    /// <summary>BMP image MIME type.</summary>
    public const string Bmp = "image/bmp";

    /// <summary>TIFF image MIME type.</summary>
    public const string Tiff = "image/tiff";

    /// <summary>EMF image MIME type.</summary>
    public const string Emf = "image/emf";

    /// <summary>Enhanced Metafile image MIME type (legacy).</summary>
    public const string EmfLegacy = "image/x-emf";

    /// <summary>WMF image MIME type.</summary>
    public const string Wmf = "image/wmf";

    /// <summary>Metafile image MIME type (legacy).</summary>
    public const string WmfLegacy = "image/x-wmf";

    /// <summary>SVG image MIME type.</summary>
    public const string Svg = "image/svg+xml";

    /// <summary>Default binary content type when unknown.</summary>
    public const string OctetStream = "application/octet-stream";
}
